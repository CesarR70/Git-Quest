# Get user input with menu options
get_input() {
    local prompt="$1"
    local valid_options=("${@:2}")
    
    while true; do
        read -p "$(echo -e "$prompt")" input
        
        # Check for special commands
        case "$input" in
            menu|Menu|MENU)
                echo "menu"
                return
                ;;
            hint|Hint|HINT)
                echo "hint"
                return
                ;;
            skip|Skip|SKIP)
                echo "skip"
                return
                ;;
            answer|Answer|ANSWER)
                echo "answer"
                return
                ;;
        esac
        
        # Check if input is in valid options (if provided)
        if [[ ${#valid_options[@]} -gt 0 ]]; then
            for opt in "${valid_options[@]}"; do
                if [[ "$input" == "$opt" ]]; then
                    echo "$input"
                    return
                fi
            done
            echo -e "${RED}Invalid input. Try again or type 'menu' to go back.${NC}"
        else
            echo "$input"
            return
        fi
    done
}

# Yes/No confirmation
confirm() {
    local prompt="$1"
    while true; do
        read -p "$(echo -e "$prompt [y/n]: ")" answer
        case "$answer" in
            y|Y|yes|Yes|YES)
                return 0
                ;;
            n|N|no|No|NO)
                return 1
                ;;
            *)
                echo -e "${YELLOW}Please answer yes or no.${NC}"
                ;;
        esac
    done
}

