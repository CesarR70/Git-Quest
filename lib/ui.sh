# Draw a horizontal line
draw_line() {
    local length="${1:-60}"
    local char="${2:-‚îÄ}"
    local i=0
    while [[ $i -lt $length ]]; do
        echo -n "$char"
        ((i++))
    done
    echo ""
}

# Draw a box with content
draw_box() {
    local title="$1"
    local content="$2"
    local width="${3:-60}"
    
    # Top border
    echo -e "${CYAN}‚ïî$(draw_line $width "‚ïê")‚ïó${NC}"
    
    # Title (if provided)
    if [[ -n "$title" ]]; then
        local padding=$(( (width - ${#title} - 2) / 2 ))
        echo -ne "${CYAN}‚ïë${NC}"
        local i=0
        while [[ $i -lt $padding ]]; do echo -n " "; ((i++)); done
        echo -e "${YELLOW}$title${NC}"
        local i=0
        while [[ $i -lt $((width - padding - ${#title} - 1)) ]]; do echo -n " "; ((i++)); done
        echo -e "${CYAN}‚ïë${NC}"
        echo -e "${CYAN}‚ï†$(draw_line $width "‚îÄ")‚ï£${NC}"
    else
        echo -e "${CYAN}‚ï†$(draw_line $width "‚îÄ")‚ï£${NC}"
    fi
    
    # Content
    echo -e "${CYAN}‚ïë${NC} $content"
    
    # Bottom border
    echo -e "${CYAN}‚ïö$(draw_line $width "‚ïê")‚ïù${NC}"
}

